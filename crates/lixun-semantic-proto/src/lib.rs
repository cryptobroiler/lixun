    /// Two-codec duplex test: encode with one side's codec, decode
    /// with the other's, simulating the wire.
    #[test]
    fn duplex_codec_cmd_then_msg() {
        let mut daemon: DaemonCodec = FrameCodec::new();
        let mut worker: WorkerCodec = FrameCodec::new();

        let cmd = Cmd::SearchText {
            req_id: 42,
            query: "hello".into(),
            k: 3,
        };
        let cmd_json = serde_json::to_string(&cmd).unwrap();
        let mut buf = BytesMut::new();
        daemon.encode(cmd, &mut buf).expect("encode cmd");

        let decoded = worker.decode(&mut buf).expect("decode cmd").expect("frame");
        assert_eq!(serde_json::to_string(&decoded).unwrap(), cmd_json);
        assert!(buf.is_empty());

        let msg = Msg::SearchResult {
            req_id: 42,
            hits: vec![AnnHit {
                doc_id: "d1".into(),
                distance: 0.1,
            }],
        };
        let msg_json = serde_json::to_string(&msg).unwrap();
        let mut buf = BytesMut::new();
        worker.encode(msg, &mut buf).expect("encode msg");
        let decoded = daemon.decode(&mut buf).expect("decode msg").expect("frame");
        assert_eq!(serde_json::to_string(&decoded).unwrap(), msg_json);
        assert!(buf.is_empty());
    }

    /// Fragmented input: the decoder must hold state across partial
    /// reads and only emit a frame when all bytes have arrived.
    #[test]
    fn codec_handles_fragmented_input() {
        let mut daemon: DaemonCodec = FrameCodec::new();
        let mut worker: WorkerCodec = FrameCodec::new();

        let cmd = Cmd::Embed {
            docs: vec![UpsertedDoc {
                doc_id: "d1".into(),
                source_instance: "fs".into(),
                mtime: 1,
                mime: None,
                body: Some("x".repeat(200)),
            }],
        };
        let cmd_json = serde_json::to_string(&cmd).unwrap();
        let mut full = BytesMut::new();
        daemon.encode(cmd, &mut full).expect("encode");
        let full_vec: Vec<u8> = full.to_vec();

        let mut buf = BytesMut::new();
        for chunk in full_vec.chunks(7) {
            buf.extend_from_slice(chunk);
            match worker.decode(&mut buf).expect("decode") {
                Some(decoded) => {
                    assert_eq!(serde_json::to_string(&decoded).unwrap(), cmd_json);
                    assert!(buf.is_empty());
                    return;
                }
                None => continue,
            }
        }
        panic!("frame never decoded after feeding all bytes");
    }

    /// Reject frames whose declared version is below the minimum.
    #[test]
    fn codec_rejects_too_old_version() {
        if MIN_PROTOCOL_VERSION == 0 {
            return;
        }
        let mut buf = BytesMut::new();
        buf.put_u32(2);
        buf.put_u16(MIN_PROTOCOL_VERSION - 1);

        let mut codec: WorkerCodec = FrameCodec::new();
        let err = codec.decode(&mut buf).expect_err("must reject");
        assert_eq!(err.kind(), std::io::ErrorKind::InvalidData);
    }

    /// Reject a length prefix smaller than the version field.
    #[test]
    fn codec_rejects_truncated_length() {
        let mut buf = BytesMut::new();
        buf.put_u32(1);

        let mut codec: WorkerCodec = FrameCodec::new();
        let err = codec.decode(&mut buf).expect_err("must reject");
        assert_eq!(err.kind(), std::io::ErrorKind::InvalidData);
    }
