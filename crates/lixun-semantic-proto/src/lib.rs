//! Wire protocol between `lixund` and the `lixun-semantic-worker`
//! sidecar.
//!
//! ## Frame format
//!
//! Mirrors the framing used by `lixun-ipc`:
//!
//! ```text
//! +-------------------+--------------------+--------------------+
//! | u32 BE total_len  | u16 BE proto_ver   | JSON payload       |
//! |   (= 2 + len(JSON)) |   = PROTOCOL_VERSION | (UTF-8, no LF)  |
//! +-------------------+--------------------+--------------------+
//! ```
//!
//! `total_len` covers the version field plus the JSON body but does
//! not include itself. Receivers that see a version outside
//! `MIN_PROTOCOL_VERSION..=PROTOCOL_VERSION` close the connection.
//!
//! ## Direction
//!
//! Two parties talk in both directions on a single Unix socket:
//!
//! - The **daemon** sends [`Cmd`] (commands the worker must execute)
//!   and receives [`Msg`] (worker-initiated messages, including RPC
//!   responses and DocStore callbacks).
//! - The **worker** sends [`Msg`] and receives [`Cmd`].
//!
//! Each side uses a [`FrameCodec<I, O>`] parameterised by what it
//! decodes (`I`) and what it encodes (`O`). The two sides mirror each
//! other:
//!
//! | side    | reads (`I`) | writes (`O`)     |
//! |---------|-------------|------------------|
//! | daemon  | [`Msg`]     | [`Cmd`]          |
//! | worker  | [`Cmd`]     | [`Msg`]          |
//!
//! ## Request/response correlation
//!
//! Every command that expects a reply carries a `req_id: u64`
//! generated by the sender. The reply [`Msg`] echoes the same
//! `req_id`. Fire-and-forget commands (e.g. [`Cmd::Embed`]) carry
//! `req_id: 0` and never produce a reply on the happy path; failures
//! still surface via [`Msg::Error`] with `req_id: 0`.

use std::marker::PhantomData;

use bytes::{Buf, BufMut, BytesMut};
use serde::{Deserialize, Serialize, de::DeserializeOwned};
use tokio_util::codec::{Decoder, Encoder};

use lixun_core::Hit;
use lixun_mutation::{AnnHit, Modality, UpsertedDoc};

pub const PROTOCOL_VERSION: u16 = 2;
pub const MIN_PROTOCOL_VERSION: u16 = 2;

/// Daemon → worker. Either a fire-and-forget mutation
/// ([`Cmd::Embed`], [`Cmd::Delete`]) or a request expecting a single
/// matching [`Msg`] reply correlated by `req_id`.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "kind", rename_all = "snake_case")]
pub enum Cmd {
    /// First frame on a fresh connection. Worker replies with
    /// [`Msg::HandshakeOk`] or closes.
    Handshake { proto_version: u16 },
    /// Fire-and-forget batch enqueue.
    Embed { docs: Vec<UpsertedDoc> },
    /// Fire-and-forget delete.
    Delete { doc_id: String },
    /// ANN search by text query. Worker embeds the query and returns
    /// [`Msg::SearchResult`] with up to `k` hits.
    SearchText { req_id: u64, query: String, k: u32 },
    /// ANN search by image — same shape as [`Cmd::SearchText`] but
    /// routes through the image embedder/channel.
    SearchImage { req_id: u64, query: String, k: u32 },
    /// Anchor-based query modality classification. Worker embeds the
    /// query via CLIP text encoder, computes cosine against pre-embedded
    /// image/text anchors, returns [`Msg::ClassifyResult`].
    ClassifyQuery { req_id: u64, query: String },
    /// Trigger a backfill walk. Worker drives the loop and pulls
    /// missing docs via callback messages
    /// ([`Msg::CallAllDocIds`] etc.). Reply on completion is
    /// [`Msg::BackfillComplete`].
    BackfillStart { req_id: u64 },
    /// Reply to a [`Msg::CallAllDocIds`] / [`Msg::CallHydrateDoc`] /
    /// [`Msg::CallGetBody`] callback. `req_id` matches the callback's
    /// `req_id`.
    CallbackReply { req_id: u64, resp: CallbackResp },
    /// Graceful shutdown — worker should drain pending writes, close
    /// LanceDB, then exit 0.
    Shutdown,
}

/// Worker → daemon. RPC replies, callbacks, async events.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "kind", rename_all = "snake_case")]
pub enum Msg {
    /// Reply to [`Cmd::Handshake`]. `proto_version` is the worker
    /// build's `PROTOCOL_VERSION`; daemon enforces version compat.
    HandshakeOk {
        proto_version: u16,
        worker_version: String,
    },
    /// Reply to [`Cmd::SearchText`] or [`Cmd::SearchImage`].
    SearchResult { req_id: u64, hits: Vec<AnnHit> },
    /// Reply to [`Cmd::ClassifyQuery`].
    ClassifyResult { req_id: u64, modality: Modality },
    /// Worker → daemon DocStore callback: list every indexed doc id.
    CallAllDocIds { req_id: u64 },
    /// Worker → daemon DocStore callback: hydrate one doc by id.
    CallHydrateDoc { req_id: u64, doc_id: String },
    /// Worker → daemon DocStore callback: fetch the stored body of a
    /// doc.
    CallGetBody { req_id: u64, doc_id: String },
    /// Reply to [`Cmd::BackfillStart`] when the walk finishes.
    BackfillComplete {
        req_id: u64,
        submitted: u64,
        total: u64,
    },
    /// Generic failure — `req_id == 0` means the failure was not
    /// tied to a specific outstanding request (e.g. a write batch
    /// failed mid-flush).
    Error {
        req_id: u64,
        code: ErrorCode,
        detail: String,
    },
}

/// Daemon's reply to a worker-initiated DocStore callback.
#[derive(Debug, Clone, Serialize, Deserialize)]
#[serde(tag = "kind", rename_all = "snake_case")]
pub enum CallbackResp {
    AllDocIds {
        ids: Vec<String>,
    },
    /// `hit` is `None` when the doc is gone (race with a delete).
    /// `ScoreBreakdown` from `DocStore::hydrate_doc` is intentionally
    /// not part of this payload: the only consumer (semantic
    /// backfill) does not read it, so keeping it off the wire avoids
    /// adding `serde` derives to `lixun_core::ScoreBreakdown`.
    HydrateDoc {
        hit: Option<Hit>,
    },
    GetBody {
        body: Option<String>,
    },
    Error {
        code: ErrorCode,
        detail: String,
    },
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum ErrorCode {
    /// Wrong/unknown `req_id`, malformed payload past the JSON layer.
    BadRequest,
    /// Embedder failure, store failure, anything the worker classes
    /// as transient.
    Internal,
    /// LanceDB / journal IO failure.
    Storage,
    /// Daemon's DocStore callback returned an error or timed out.
    Callback,
    /// Protocol version mismatch on handshake.
    Version,
}

#[derive(Default)]
enum DecodeState {
    #[default]
    Header,
    Version(usize),
    Payload(usize),
}

/// Length-prefixed JSON codec parameterised by what each side
/// decodes (`I`) and what it encodes (`O`).
pub struct FrameCodec<I, O> {
    state: DecodeState,
    _phantom: PhantomData<fn() -> (I, O)>,
}

impl<I, O> Default for FrameCodec<I, O> {
    fn default() -> Self {
        Self {
            state: DecodeState::Header,
            _phantom: PhantomData,
        }
    }
}

impl<I, O> FrameCodec<I, O> {
    pub fn new() -> Self {
        Self::default()
    }
}

impl<I, O> Encoder<O> for FrameCodec<I, O>
where
    O: Serialize,
{
    type Error = std::io::Error;

    fn encode(&mut self, item: O, dst: &mut BytesMut) -> Result<(), Self::Error> {
        let json = serde_json::to_vec(&item)
            .map_err(|e| std::io::Error::new(std::io::ErrorKind::InvalidData, e))?;
        let total_len = 2 + json.len();
        if total_len > u32::MAX as usize {
            return Err(std::io::Error::new(
                std::io::ErrorKind::InvalidInput,
                "frame too large",
            ));
        }
        dst.reserve(4 + total_len);
        dst.put_u32(total_len as u32);
        dst.put_u16(PROTOCOL_VERSION);
        dst.put_slice(&json);
        Ok(())
    }
}

impl<I, O> Decoder for FrameCodec<I, O>
where
    I: DeserializeOwned,
{
    type Item = I;
    type Error = std::io::Error;

    fn decode(&mut self, src: &mut BytesMut) -> Result<Option<Self::Item>, Self::Error> {
        loop {
            match self.state {
                DecodeState::Header => {
                    if src.len() < 4 {
                        return Ok(None);
                    }
                    let len = src.get_u32() as usize;
                    if len < 2 {
                        return Err(std::io::Error::new(
                            std::io::ErrorKind::InvalidData,
                            "frame too short for version",
                        ));
                    }
                    self.state = DecodeState::Version(len);
                }
                DecodeState::Version(len) => {
                    if src.len() < 2 {
                        return Ok(None);
                    }
                    let version = src.get_u16();
                    if !(MIN_PROTOCOL_VERSION..=PROTOCOL_VERSION).contains(&version) {
                        return Err(std::io::Error::new(
                            std::io::ErrorKind::InvalidData,
                            format!(
                                "version {} outside supported {}..={}",
                                version, MIN_PROTOCOL_VERSION, PROTOCOL_VERSION
                            ),
                        ));
                    }
                    self.state = DecodeState::Payload(len - 2);
                }
                DecodeState::Payload(len) => {
                    if src.len() < len {
                        return Ok(None);
                    }
                    let data = src.split_to(len);
                    let item: I = serde_json::from_slice(&data)
                        .map_err(|e| std::io::Error::new(std::io::ErrorKind::InvalidData, e))?;
                    self.state = DecodeState::Header;
                    return Ok(Some(item));
                }
            }
        }
    }
}

/// Codec for the daemon side: reads [`Msg`], writes [`Cmd`].
pub type DaemonCodec = FrameCodec<Msg, Cmd>;

/// Codec for the worker side: reads [`Cmd`], writes [`Msg`].
pub type WorkerCodec = FrameCodec<Cmd, Msg>;

