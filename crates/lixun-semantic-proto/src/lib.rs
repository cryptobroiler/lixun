    /// JSON-string equality is used instead of `PartialEq` because
    /// the wire payload includes types from `lixun-core` and
    /// `lixun-mutation` (e.g. `Hit`, `AnnHit`) that intentionally do
    /// not derive `PartialEq`. Stable JSON via `to_string` + parsed
    /// re-encode is sufficient to prove the round-trip is lossless.
    fn round_trip<T>(value: T)
    where
        T: Serialize + DeserializeOwned,
    {
        let s1 = serde_json::to_string(&value).expect("serialize");
        let back: T = serde_json::from_str(&s1).expect("deserialize");
        let s2 = serde_json::to_string(&back).expect("re-serialize");
        assert_eq!(s1, s2, "round-trip mismatch");
    }

